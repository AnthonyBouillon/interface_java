package subheading_management;


public class Subheading {

    private int id;
    private String name;
    private int id_heading;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getId_heading() {
        return id_heading;
    }

    public void setId_heading(int id_heading) {
        this.id_heading = id_heading;
    }


}
